function generateShoppingList(items) {
  const all = { meat: [], veg: [], other: [] }

  items.forEach(item => {
    const ingredients = INGREDIENT_MAP[item.name]
    if (!ingredients) return

    const qty = item.quantity || 1

    const addIngredient = (category, ingredient) => {
      const adjusted = qty > 1 ? ingredient.replace(/(\d+)/, (match) => {
        return parseInt(match) * qty
      }) : ingredient
      if (!all[category].includes(adjusted)) {
        all[category].push(adjusted)
      }
    }

    ingredients.meat.forEach(i => addIngredient('meat', i))
    ingredients.veg.forEach(i => addIngredient('veg', i))
    ingredients.other.forEach(i => addIngredient('other', i))
  })

  const result = []
  if (all.meat.length > 0) result.push({ type: 'meat', label: '🥩 肉类 / 海鲜', items: all.meat })
  if (all.veg.length > 0) result.push({ type: 'veg', label: '🥬 蔬菜 / 水果', items: all.veg })
  if (all.other.length > 0) result.push({ type: 'other', label: '🧂 调料 / 其他', items: all.other })
  return result
}
